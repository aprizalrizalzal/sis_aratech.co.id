<script setup>
import { ref, computed } from 'vue';
import { useForm, usePage } from '@inertiajs/vue3';
import jsPDF from 'jspdf';
import 'jspdf-autotable';
import PartUsageForm from '@/Pages/PartUsage/PartUsageForm.vue';
import SecondaryButton from '@/Components/SecondaryButton.vue';
import DangerButton from '@/Components/DangerButton.vue';
import Modal from '@/Components/Modal.vue';
import DateTimePicker from '@/Components/DateTimePicker.vue';
import PrimaryButton from '@/Components/PrimaryButton.vue';
import PrinterIcon from '@/Components/Icon/PrinterIcon.vue';
import ButtonImage from '@/Components/ButtonImage.vue';
import BackIcon from '@/Components/Icon/BackIcon.vue';
import NextIcon from '@/Components/Icon/NextIcon.vue';



const page = usePage();

const props = defineProps({
    partUsages: Array,
    serviceDetails: Array,
    statusServices: Array,
    spareParts: Array,
});

const formatCurrency = (value) => {
    return new Intl.NumberFormat('id-ID', { style: 'currency', currency: 'IDR', minimumFractionDigits: 0, maximumFractionDigits: 0 }).format(value);
};

const showingModalPartUsageUpdate = ref(false);
const selectedPartUsage = ref(null);
const selectedServiceDetail = ref(null);
const selectedSparePart = ref(null);

const showModalPartUsageUpdate = (partUsage) => {
    selectedPartUsage.value = partUsage;
    selectedServiceDetail.value = partUsage.service_detail;
    selectedSparePart.value = partUsage.spare_part;
    showingModalPartUsageUpdate.value = true;
};

const confirmingPartUsageDeletion = ref(false);

const form = useForm({
    id: null,
});

const confirmPartUsageDeletion = (partUsageId) => {
    confirmingPartUsageDeletion.value = true;
    form.id = partUsageId;
};

const deletePartUsage = () => {
    form.delete(route('destroy.part.usage'), {
        preserveScroll: true,
        onSuccess: () => closeModal(),
        onError: (errors) => {
            if (errors) {
                closeModal();
            } else {
                const errorMessages = Object.values(errors).flat();
                alert(`${errorMessages}`);
            }
        }
    });
};

const showingModalUpdateSuccessfully = ref(false);

const showModalUpdateSuccessfully = () => {
    showingModalPartUsageUpdate.value = false;
    showingModalUpdateSuccessfully.value = true;
};

const closeModalUpdateSuccessfully = () => {
    showingModalUpdateSuccessfully.value = false;
};

const closeModal = () => {
    showingModalPartUsageUpdate.value = false;
    confirmingPartUsageDeletion.value = false;
};

const start_date = ref('');
const end_date = ref('');

const defaultStartDate = new Date();
defaultStartDate.setDate(defaultStartDate.getDate() - 30);
const defaultEndDate = new Date();

start_date.value = defaultStartDate;
end_date.value = defaultEndDate;

// Add unique keys for DateTimePickers
const datePickerKeys = ref({
    startDate: 0,
    endDate: 0,
});

const resetDateFilters = () => {
    start_date.value = defaultStartDate;
    end_date.value = defaultEndDate;

    // Update the keys to force re-render
    datePickerKeys.value.startDate += 1;
    datePickerKeys.value.endDate += 1;
};

const currentPage = ref(1);
const itemsPerPage = 15;

const filteredPartUsages = computed(() => {
    let partUsages = props.partUsages;

    if (start_date.value || end_date.value) {
        partUsages = partUsages.filter(serviceDetail => {
            const createdDate = new Date(serviceDetail.created_at);
            const start = start_date.value ? new Date(start_date.value) : null;
            const end = end_date.value ? new Date(end_date.value) : null;

            if (start && end) {
                return createdDate >= start && createdDate <= end;
            } else if (start) {
                return createdDate >= start;
            } else if (end) {
                return createdDate <= end;
            }

            return true;
        });
    }

    return partUsages;
});

const paginatedPartUsages = computed(() => {
    const start = (currentPage.value - 1) * itemsPerPage;
    const end = start + itemsPerPage;
    return filteredPartUsages.value.slice(start, end);
});

const totalPages = computed(() => {
    return Math.ceil(filteredPartUsages.value.length / itemsPerPage);
});

const nextPage = () => {
    if (currentPage.value < totalPages.value) {
        currentPage.value++;
    }
};

const previousPage = () => {
    if (currentPage.value > 1) {
        currentPage.value--;
    }
};

const totalPrice = computed(() => {
    return filteredPartUsages.value.reduce((total, partUsage) => {
        const price = parseFloat(partUsage.spare_part.price);
        return total + (isNaN(price) ? 0 : price);
    }, 0);
});

const totalQuantity = computed(() => {
    return filteredPartUsages.value.reduce((total, partUsage) => {
        const quantity = parseFloat(partUsage.quantity);
        return total + (isNaN(quantity) ? 0 : quantity);
    }, 0);
});

function formatDate(dateString) {
    const date = new Date(dateString);

    const options = {
        weekday: 'long',
        year: 'numeric',
        month: 'long',
        day: 'numeric',
    };

    return date.toLocaleDateString('id-ID', options);
}

const printContent = ref(null);

const handlePrint = () => {
    const pdf = new jsPDF({
        format: 'a3',
        orientation: 'portrait',
    });

    const printContentEl = printContent.value;
    const headerCells = printContentEl.querySelectorAll('thead th');
    const columns = Array.from(headerCells).map(th => th.innerText);

    const logoImage = new Image();
    logoImage.src = 'storage/images/header/company_logo.png'; // Ganti dengan path gambar logo Anda
    pdf.addImage(logoImage, 'PNG', 15, 15, 25, 25); // Menambahkan gambar logo dengan posisi dan ukuran

    const headers = page.props.headers;
    headers.forEach(header => {
        pdf.setFontSize(18);
        pdf.text(`SIService - ${header.company}`, pdf.internal.pageSize.width / 7, 22);  // Menambahkan nama perusahaan
        pdf.setFontSize(14);
        pdf.text(`${header.description}`, pdf.internal.pageSize.width / 7, 30);  // Menambahkan nama perusahaan
    });

    pdf.setTextColor(0, 125, 0);
    pdf.setFontSize(12);
    pdf.text('Part Usages Report', 42, 38);
    pdf.setFontSize(12);
    pdf.text(`${formatDate(start_date.value)} - ${formatDate(end_date.value)}`, pdf.internal.pageSize.width - 14, 38, { align: 'right' });

    const rows = [];
    const content = printContentEl.querySelectorAll('tbody tr');
    content.forEach((row) => {
        const cells = row.querySelectorAll('td');
        const rowData = Array.from(cells).map(cell => cell.innerText);
        rows.push(rowData);
    });

    const columnWidths = [10, 50, 140, 50, 20];

    pdf.autoTable({
        head: [columns],
        body: rows,
        startY: 42,
        styles: { font: 'helvetica', fontSize: 10 },
        columnStyles: {
            // Specify styles for each column
            0: { cellWidth: columnWidths[0] },
            1: { cellWidth: columnWidths[1] },
            2: { cellWidth: columnWidths[2] },
            3: { cellWidth: columnWidths[3] },
            4: { cellWidth: columnWidths[4] },
        },
        theme: 'grid',
    });

    const totalPages = pdf.internal.getNumberOfPages();
    const timestamp = new Date().getTime();

    const columnTotalWidths = [200, 50, 20];
    const startY = pdf.autoTable.previous.finalY;
    pdf.autoTable({
        body: [
            ['Total', formatCurrency(totalPrice.value), totalQuantity.value],
        ],
        startY: startY,
        styles: { fontSize: 10, fontStyle: 'bold' },
        columnStyles: {
            0: { cellWidth: columnTotalWidths[0] },
            1: { cellWidth: columnTotalWidths[1] },
            2: { cellWidth: columnTotalWidths[2] },
        },
        theme: 'grid',
    });

    pdf.setPage(totalPages);
    pdf.setFontSize(8);
    pdf.text(`Generated on ${new Date().toLocaleString('en-US')} / ${timestamp} by ${page.props.auth.user.name}`, pdf.internal.pageSize.getWidth() - 20, pdf.internal.pageSize.getHeight() - 10, { align: 'right' });

    const blob = pdf.output('blob');
    const pdfURL = URL.createObjectURL(blob);

    const newTabPdf = window.open('', '_blank');
    newTabPdf.location.href = pdfURL;
};
</script>

<template>
    <div class="flex w-full gap-2 justify-between my-4">
        <div class="flex items-center gap-2 bg-white">
            <DateTimePicker :key="datePickerKeys.startDate" id="start_date" label="Start Date" v-model="start_date"
                placeholder="Select Start Date Time" />
            <DateTimePicker :key="datePickerKeys.endDate" id="end_date" label="End Date" v-model="end_date"
                placeholder="Select End Date Time" />
        </div>
        <div class="my-auto">
            <PrimaryButton @click="resetDateFilters"><span class="py-1 px-3">Reset</span></PrimaryButton>
        </div>
    </div>
    <div class=" overflow-x-auto">
        <table class="min-w-full bg-white border-collapse">
            <thead>
                <tr>
                    <th class="py-4 px-4 border-b border-green-200 bg-green-200 truncate">No</th>
                    <th class="py-4 px-4 border-b border-green-200 bg-green-200 text-left truncate">Service Detail
                        Code
                    </th>
                    <th class="py-4 px-4 border-b border-green-200 bg-green-200 text-left truncate">Spare Part</th>
                    <th class="py-4 px-4 border-b border-green-200 bg-green-200 text-left truncate">Price</th>
                    <th class="py-4 px-4 border-b border-green-200 bg-green-200 truncate">Quantity</th>
                    <th class="py-4 px-4 border-b border-green-200 bg-green-200" colspan="2">Action</th>
                </tr>
            </thead>
            <tbody>
                <tr v-for="(partUsage, index) in paginatedPartUsages" :key="partUsage.id" class="hover:bg-green-50">
                    <td class="py-2 px-4 border-b border-green-200 text-center">{{ (currentPage - 1) * itemsPerPage +
                        index + 1 }}</td>
                    <td class="py-2 px-4 border-b border-green-200 truncate max-w-xs">{{
                        partUsage.service_detail.service_detail_code }}</td>
                    <td class="py-2 px-4 border-b border-green-200 truncate max-w-xs">
                        {{ partUsage.spare_part.name }}</td>
                    <td class="py-2 px-4 border-b border-green-200 truncate">{{
                        formatCurrency(partUsage.spare_part.price) }}</td>
                    <td class="py-2 px-4 border-b border-green-200 text-center">{{ partUsage.quantity }}</td>
                    <td class="py-2 px-4 border-b border-green-200 text-center">
                        <SecondaryButton @click="showModalPartUsageUpdate(partUsage)">Update
                        </SecondaryButton>
                    </td>
                    <td class="py-2 px-4 border-b border-green-200 text-center">
                        <DangerButton @click="confirmPartUsageDeletion(partUsage.id)">Delete</DangerButton>
                    </td>
                </tr>
            </tbody>
        </table>
    </div>

    <div class="flex flex-col w-full text-center">
        <div class="flex flex-row w-full">
            <p class="py-2 px-4 border-b border-green-200 font-semibold w-full">Total Price</p>
            <p class="py-2 px-4 border-b border-green-200 font-semibold w-full">{{ formatCurrency(totalPrice)
                }}</p>
        </div>
        <div class="flex flex-row w-full">
            <p class="py-2 px-4 border-b border-green-200 font-semibold w-full">Total Quantity</p>
            <p class="py-2 px-4 border-b border-green-200 font-semibold w-full">{{ totalQuantity }}</p>
        </div>
    </div>

    <div class="flex justify-center gap-4 items-center p-6">
        <ButtonImage class="py-2" @click="previousPage" :disabled="currentPage === 1">
            <BackIcon />
        </ButtonImage>
        <span>{{ currentPage }} / {{ totalPages }}</span>
        <ButtonImage class="py-2" @click="nextPage" :disabled="currentPage === totalPages">
            <NextIcon />
        </ButtonImage>
    </div>

    <SecondaryButton @click="handlePrint" class="w-full my-4">
        <PrinterIcon /><span class="py-1 w-full">Print / pdf</span>
    </SecondaryButton>

    <div ref="printContent" class="overflow-x-auto" style="display: none;">
        <table class="min-w-full bg-white border-collapse">
            <thead>
                <tr>
                    <th class="py-4 px-4 border-b border-green-200 bg-green-200">No</th>
                    <th class="py-4 px-4 border-b border-green-200 bg-green-200">Service Detail Code</th>
                    <th class="py-4 px-4 border-b border-green-200 bg-green-200">Spare Part</th>
                    <th class="py-4 px-4 border-b border-green-200 bg-green-200">Price</th>
                    <th class="py-4 px-4 border-b border-green-200 bg-green-200">Quantity</th>
                </tr>
            </thead>
            <tbody>
                <tr v-for="(partUsage, index) in paginatedPartUsages" :key="partUsage.id" class="hover:bg-green-50">
                    <td class="py-2 px-4 border-b border-green-200 text-center">{{ (currentPage - 1) * itemsPerPage +
                        index + 1 }}</td>
                    <td class="py-2 px-4 border-b border-green-200 text-center">{{
                        partUsage.service_detail.service_detail_code }}</td>
                    <td class="py-2 px-4 border-b border-green-200 text-center ">{{ partUsage.spare_part.name }}</td>
                    <td class="py-2 px-4 border-b border-green-200 text-center">{{
                        formatCurrency(partUsage.spare_part.price) }}</td>
                    <td class="py-2 px-4 border-b border-green-200 text-center">{{ partUsage.quantity }}</td>
                </tr>
            </tbody>
        </table>
    </div>

    <Modal :show="showingModalPartUsageUpdate">
        <div class="m-6">
            <div class="flex justify-between items-center ps-6 ms-6 text-green-800">
                <span class="font-bold text-center w-full">Update Part Usage</span>
                <DangerButton @click="closeModal">X</DangerButton>
            </div>
            <hr class="mt-4 mb-2">
            <PartUsageForm :partUsage="selectedPartUsage" :serviceDetails="serviceDetails" :spareParts="spareParts"
                :serviceDetail="selectedPartUsage.service_detail" :sparePart="selectedPartUsage.spare_part" :statusServices="statusServices"
                @updatePartUsage="showModalUpdateSuccessfully" />
        </div>
    </Modal>

    <Modal maxWidth="xl" :show="showingModalUpdateSuccessfully">
        <div class="m-6">
            <div class="flex justify-between items-center ps-6 ms-6 text-green-800">
                <span class="font-bold text-center w-full">Update Part Usage</span>
                <DangerButton @click="closeModalUpdateSuccessfully">X</DangerButton>
            </div>
            <hr class="mt-4 mb-2">
            <p class="my-4 text-sm text-green-600">
                Part Usage Update Successful!
            </p>
            <div class="mt-2 flex">
                <a :href="route('show.service.details')" class="inline-flex items-center px-4 py-2 bg-green-600 border border-transparent rounded font-semibold text-xs text-white uppercase tracking-widest hover:bg-green-800 focus:bg-green-600 active:bg-green-600 focus:outline-none focus:ring-2 focus:ring-green-600 focus:ring-offset-2 transition ease-in-out duration-150" @click="closeModalUpdateSuccessfully">Ok</a>
            </div>
        </div>
    </Modal>

    <Modal :show="confirmingPartUsageDeletion">
        <div class="p-6">
            <h2 class="text-lg font-medium text-green-800">
                Are you sure you want to delete your PartUsage?
            </h2>
            <p class="mt-1 text-sm text-green-600">
                Once your PartUsage is deleted, all of its resources and data will be permanently deleted.
            </p>
            <div class="mt-6 flex justify-end">
                <SecondaryButton @click="closeModal">Cancel</SecondaryButton>
                <DangerButton class="ms-3" :class="{ 'opacity-25': form.processing }" :disabled="form.processing"
                    @click="deletePartUsage">
                    Delete Part Usage
                </DangerButton>
            </div>
        </div>
    </Modal>
</template>

